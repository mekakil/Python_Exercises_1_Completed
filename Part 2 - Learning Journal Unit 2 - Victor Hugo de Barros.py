def square_root(sideone, sidetwo): #Function for calculating the square-root of a square.
    x = sideone   #To inform the value of side one.
    y = sidetwo   #To inform the value of side two.
    total = x * y  #multiplication for obtain the square-root.
    return total  #to inform the value that in which has to be returned.

print(square_root(2, 4)) #First argument, call.
print(square_root(242, 333)) #First argument, call.
print(square_root(211, 432)) #Third argument, call.

